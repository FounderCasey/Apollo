import React, { useState, useEffect } from "react";
import db from "../firebase.config";
import "../styles/ConnectWallet.scss";

function ConnectWallet() {
  const [currentAccount, setCurrentAccount] = useState("");

  const checkIfWalletIsConnected = async () => {
    try {
      const { ethereum } = window;

      if (!ethereum) {
        console.log("Make sure you have metamask!");
        return;
      } else {
        console.log("We have the ethereum object", ethereum);
      }

      const accounts = await ethereum.request({ method: "eth_accounts" });

      if (accounts.length !== 0) {
        const account = accounts[0];
        console.log("Found an authorized account:", account);
        setCurrentAccount(account);
      } else {
        console.log("No authorized account found");
      }
    } catch (error) {
      console.log(error);
    }
  };

  const connectWallet = async () => {
    try {
      const { ethereum } = window;

      if (!ethereum) {
        alert("Get MetaMask!");
        return;
      }

      const accounts = await ethereum.request({
        method: "eth_requestAccounts",
      });

      console.log("Connected", accounts[0]);

      await setCurrentAccount(accounts[0]);
      await db.collection("users").doc(accounts[0]).set({
        purchased: [],
      });
    } catch (error) {
      console.log(error);
    }
  };

  const checkFirebaseUser = async () => {
    const snapshot = db.collection("users");
    const data = await snapshot.get();
    console.log(data.docs);
    data.docs.forEach((element) => {
      if (element.id === currentAccount) console.log("we found it");
    });
  };

  useEffect(() => {
    checkIfWalletIsConnected();
  }, []);

  return (
    !currentAccount && (
      <button className="connectWallet" onClick={connectWallet}>
        Connect
      </button>
    )
  );
}

export default ConnectWallet;
